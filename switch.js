const month = 1;
switch (month) {
  case 1:
    console.log(`January`);
    break;
  case 2:
    console.log(`Feburary`);
    break;
  case 3:
    console.log(`March`);
    break;
  case 4:
    console.log(`April`);
    break;
  case 5:
    console.log(`May`);
    break;
  case 6:
    console.log(`June`);
    break;

  default:
    console.log(`default`)
    break;
}
